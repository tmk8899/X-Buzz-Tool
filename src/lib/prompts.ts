import type { GeneratePostInput } from "@/types/generate";

/**
 * バズ投稿型ごとの構造指示
 * プロンプトに直接埋め込まれるため、AIへの指示として機能する
 */
const TONE_INSTRUCTIONS: Record<string, string> = {
  problem: `【問題提起型】
構造: ① 読者が共感する「あるある問題」を冒頭で提示 → ② その原因 → ③ 解決策の提示
ポイント: 「〜で悩んでいませんか？」「実は〇〇が原因です」など共感ワードを使う。
問題→原因→解決の流れを守り、最後は希望を見せる。`,

  contrast: `【対比型】
構造: ① 「AとBを比較」「〇〇な人 vs △△な人」の対比軸を設定 → ② 具体的な違いを列挙 → ③ 読者への問いかけ or 気づき
ポイント: 対比は2〜3点に絞る。数字・具体例を使うと説得力UP。
「〇〇する人 / しない人」「1年前の自分 / 今の自分」などの軸が効果的。`,

  story: `【体験談型】
構造: ① 強い実体験フック（結果から入るのが有効） → ② 状況・背景の説明 → ③ ターニングポイント → ④ 学び・教訓
ポイント: 「私が〇〇した結果…」「あのとき〇〇していなかったら…」など一人称で語る。
感情の変化（不安→成功）を盛り込み、読者が「自分事」と感じるように書く。`,

  list: `【箇条書き型】
構造: ① 「〇〇な人が必ずやる□個のこと」などリスト全体の価値を示すタイトル → ② 番号付きリスト（各項目に一言説明） → ③ まとめの一言
ポイント: 項目数は3〜7個が最適。各項目は10〜20文字で簡潔に。
スキャンして価値がわかるよう、項目の冒頭に動詞を置く。`,

  conclusion: `【結論先出し型】
構造: ① 結論・答えを冒頭の1文で断言 → ② その理由・根拠（2〜3点） → ③ 具体例 or 反論への回答
ポイント: 「〇〇です。理由は3つ。」のように先に結論を置く。
権威性・数字・実績を根拠に使う。反論を先取りして潰すと信頼感UP。`,

  bait: `【煽りフック型】
構造: ① 常識を否定するか挑発的な一文でフック → ② 「え？なぜ？」という疑問を引き出す展開 → ③ 意外な答えと納得感
ポイント: 「〇〇は間違いです」「〜〜している人は損してます」のような逆張り・警告型の書き出し。
誇張しすぎず、読んだ後に「なるほど」と思わせる着地を必ず用意する。`,

  thread: `【教育スレッド型】
構造: ① 「〇〇について解説します🧵」など予告フック → ② ステップ形式 or 章立てで情報を分割 → ③ 締めのまとめ + 保存推奨CTA
ポイント: 1投稿の文字数内でも「1/」「2/」のように章立てを意識した構成にする。
「保存して読み返してください」「RTしてフォロワーにも教えてあげてください」などの保存・拡散CTAが有効。`,
};

export function buildGeneratePrompt(input: GeneratePostInput): string {
  const tone = TONE_INSTRUCTIONS[input.tone] ?? input.tone;
  const ctaText = input.hasCta
    ? "あり（コメント・RT・フォローを促す一文や問いかけを末尾に入れる）"
    : "なし（CTAは含めない）";
  const charLimit = input.maxChars ?? 140;
  const personaNote = input.persona
    ? `名前: ${input.persona.name}\nトーン補足: ${input.persona.tone ?? "指定なし"}\n得意トピック: ${(input.persona.topics ?? []).join("、")}`
    : "特になし";

  return `あなたはX投稿のプロ編集者です。
以下の条件をもとに、反応が取りやすい日本語の短文投稿を3案作成してください。

条件:
- テーマ: ${input.topic}
- ターゲット: ${input.target}
- 目的: ${input.purpose}
- 投稿の型・構造指示:
${tone}
- 文字数目安: ${charLimit}文字前後（ハッシュタグ含まず）
- CTA: ${ctaText}
- ペルソナ情報:\n${personaNote}
- 追加指示: ${input.customInstruction || "なし"}

出力ルール:
- 1案ずつ見出し付き
- 冒頭1行は強いフック
- 改行を活用
- 読みやすさ重視
- 抽象論で終わらず具体性を入れる
- 日本のXユーザー向けの自然な言い回し

---

【リライトモード】

入力された文章が与えられた場合は、新規作成ではなくリライトを優先してください。

【リライトルール】
・元の意味は変えない
・1行目を強烈なフックに変える
・語尾は断定（〜です禁止）
・無駄な文章を削る
・テンポよく改行する
・共感・違和感・意外性を強める
・自然な日本語で整える

【出力形式】
必ず以下のJSON配列のみを返してください。前後に説明文や \`\`\` は含めないこと。

[
  {
    "title": "見出し（案1のタイトル、30文字以内）",
    "hook": "冒頭フック（最初の1〜2文。読者を強く引きつける）",
    "body": "本文（改行は\\nで表現。具体性を含む）",
    "cta": "CTA文（CTAなしの場合は空文字列\"\"）",
    "hashtags": ["ハッシュタグ1", "ハッシュタグ2", "ハッシュタグ3"],
    "full_post": "hook\\n\\nbody\\n\\ncta を結合した完全な投稿文"
  },
  { ... },
  { ... }
]`;
}
